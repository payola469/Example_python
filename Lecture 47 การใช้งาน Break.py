for x in range(12):
    for y in range(12):
        print(x+1,"x", y+1, "=",(x+1)*(y+1))
    break #คำสั่งนี้คือการหยุดทำงาน
#คำตอบจะออกมาเป็นแบบนี้
# 1 x 1 = 1
# 1 x 2 = 2
# 1 x 3 = 3
# ...
# ...
# 1 x 12 = 12

# เพราะ break และ for y อยู่ในลำดับเดียวกัน
# ลำดับการทำงาน for x รอบที่ 1 > for y รอบที่ 1 > break > for x รอบที่ 2 > for y รอบที่ 2 > break > วนแบบนี้จนครับ 12 รอบ